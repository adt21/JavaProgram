package Oops;

public class Loops {

	public static void main(String[] args) {
		// for loop example
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        // while loop example
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }

        // do-while loop example
        int j = 1;
        do {
            System.out.println(j);
            j++;
        }while(j<=5);

	}

}
