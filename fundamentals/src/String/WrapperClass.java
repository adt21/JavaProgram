package String;

public class WrapperClass {

	public static void main(String[] args) {
		String str1 = "11";
		String str2 = "4";
		int i = Integer.parseInt(str1);
		int j = Integer.parseInt(str2);
		int result = i * j;
		System.out.println(result);
		
		//Wrapper class in java is a class where it wraps/contains the primitive data types
		//Wrapper class will always start with an uppercase letter
		//int -- Integer(in wrapper class)
		//char -- Character
		//byte -- Byte

	}

}
