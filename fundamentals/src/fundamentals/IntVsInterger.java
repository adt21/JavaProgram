package fundamentals;

public class IntVsInterger {

	public static void main(String[] args) {
		int i = 3;// here you can't write null as it is not a reference//int is a primitive data type
		Integer i1 = null; // here i1 is the reference // Integer is a non primitive data type
		System.out.println(i);
		System.out.println(i1);

	}

}
